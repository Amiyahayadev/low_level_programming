#include <stdio.h>
/**
 * main - entry point of program
 * @argc: count of all arguments
 * @argv: the string arguments passed from command line
 * Return: 0 on SUCCESS
 */
int main(int argc, char __attribute__ ((unused)) *argv[])
{
	/* since no of arguments passed is given by argc */
	printf("%d", argc);
	printf("\n");
	return (0);
}

